int	ft_isdigit(char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			j = 1;
		else
			return (0);
		i++;
	}
	return (j);
}